#include <iostream>
using namespace std;
int main() {
    float r, j;
    cout << "masukkan angka ke1 :";
    cin >> r;
    cout << "masukkan angka ke2 :";
    cin >> j;

    cout << "penjumlahan :" << r + j << endl;
    cout << "pengurangan :" << r - j << endl;
    cout << "perkalian :" << r * j << endl;
    cout << "pembagian :" << r/ j << endl;
    return 0;
}   